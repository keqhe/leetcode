
class Solution {
public:
    //even number of operations leads to off state
    //odd number of operations leads to on state
    //all prime numbers have even number of operations
    //non-prime numbers like 12 also have even number of operations
    //but non-prime numbers like 1, 4, 9 have odd number of operations
    int bulbSwitch(int n) {
        return sqrt(n);
    }
};
